# print is a function. It prints in terminal the value of the argument
print(1)
print(1.245)
print("stringa di caratteri alfabetici")
print(True)
print([2, 3, 5, 7, 11, 13])
print({2, 3, 5, 7, 11, 13})
print({'prima_chiave': 'primo_valore', 'seconda_chiave': 'secondo_valore'})


# type() tells us the data type of the value we passed
print(type(1))
print(type(1.245))
print(type("stringa di caratteri alfabetici"))
print(type(True))
print(type([2, 3, 5, 7, 11, 13]))
print(type({2, 3, 5, 7, 11, 13}))
print(type({'prima_chiave': 'primo_valore', 'seconda_chiave': 'secondo_valore'}))


